package com.company;

public class GameEntity {
    private int health, damage;

    public int getHeals() {
        return health;
    }

    public void setHeals(int heals) {
        this.health = heals;
    }

    public int getDamage() {
        return damage;
    }

    public void setDamage(int damage) {
        this.damage = damage;
    }
}
